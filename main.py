operation_name = int(input()) # реализуем пользовательский ввод операции
match(operation_name): # добавляем оператор выбора для значения переменной операции
    case 1 : # при случае, когда операция равна 1, то
        print("Результат сложения:",number_one + number_two) # производим сложение и выводим результат операции
    case 2: # при случае, когда операция равна 2, то
        print("Результат вычитания:",number_one - number_two) # производим вычитание и выводим результат операции
    case 3: # при случае, когда операция равна 3, то
        print("Результат умножения:", number_one * number_two) # производим умножение и выводим результат операции
